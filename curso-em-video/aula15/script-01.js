// Arrays = Variáveis Compostas = Vetores

let num = [0, 6, 4]
num[3] = 2
num.push(8) //acrescenta um novo elemento no Array

console.log(`O conteúdo do vetor: ${num}`)
console.log(`O primeiro valor do vetor: ${num[0]}`)
console.log(`O tamanho do vetor: ${num.length}`)
console.log(`O conteúdo do vetor em ordem crescente: ${num.sort()}`)